--Buscar los compañeros
select "estudiantes"."nombre", "estudiantes"."apellido_paterno", "estudiantes"."apellido_materno", "estudiantes"."edad", "profesor_id" from "estudiantes"
join "matricula" on "matricula"."estudiante_id" = "estudiantes"."id"
join "cursos" on "cursos"."id" = "matricula"."curso_id"
where "cursos"."nombre" = "Física Aplicada" and "profesor_id" = 1;

--Ordenar por edad
select "nombre", "apellido_paterno", "apellido_materno", "edad" from "estudiantes"
order by "edad" desc, "nombre";

--Inscritos por curso
select "cursos"."nombre", count("estudiantes"."id") from "estudiantes"
join "matricula" on "matricula"."estudiante_id" = "estudiantes"."id"
join "cursos" on "cursos"."id" = "matricula"."curso_id"
group by "cursos"."nombre";

create index "estudiante_nombre" on "estudiantes"("nombre");
create index "nombre_cursos" on "cursos"("nombre");
